# Strings
print("Hola Mundo")
print('Hola Mundo')
print("""Hola Mundo""")
print('''Hola Mundo''')

# Muestra la clase del dato es decir string o str
print(type("Hello World"))

# Concatenación

print("Este"+" es "+"un texto"+" concatenado!")

# Números

# Enteros

print(30)
print("El número 30 es del tipo : ")
print(type(30))

# Float

print(12.5)
print("El número 12.5 es del tipo : ")
print(type(12.5))

# Boolean

print(True)
print(False)
print(type(True))

# list

lista = [10,20,22,30]
lista2 = [10, "Hello", True]
print(lista, lista2)
print(type(lista))

# Tupla - es una lista que no puede cambiar

tupla_1 = (10,25,"wewe", True)
print(tupla_1,)
print(type(tupla_1))

# Diccionarios

dic = {
    "Nombre": "Luis", 
    "Apellido" : "Garay",
    "Edad" : "37"
}

print(dic)
print(type(dic))

# None

ninguno = None
print(ninguno)
print(type(ninguno))